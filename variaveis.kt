fun main() {
    println("Variaveis e Constantes \n")

    var exV: Int = 10
    val exC: Int = 5

    println("Variável: $exV \n")
    println("Constante: $exC \n")

    println("A diferenca entre variavel e constante é que enquanto a variavel pode ter o seu valor mudado, a constante sempre terá um valor fixo.")
}

